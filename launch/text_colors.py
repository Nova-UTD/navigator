class text_colors:
    """
    Quick colors to use for outputting to console. Used like HTML tags, end with ENDC always
    """
    HEADER = '\033[95m'
    ORANGE = '\033[1;33m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'